async function attachABI() {
  try {
    console.log(`Attempting to associate ABI with contract ${CONTRACT_ADDRESS}...`);
    
    // First check if the contract is already verified
    const checkParams = new URLSearchParams();
    checkParams.append('apikey', POLYGONSCAN_API_KEY);
    checkParams.append('module', 'contract');
    checkParams.append('action', 'getsourcecode');
    checkParams.append('address', CONTRACT_ADDRESS);
    
    const checkResponse = await axios.get(`https://api.polygonscan.com/api?${checkParams}`);
    
    if (checkResponse.data.status === '1' && 
        checkResponse.data.result[0].ABI !== "Contract source code not verified") {
      console.log("✅ Contract is already verified on Polygonscan!");
      console.log(`View on Polygonscan: https://polygonscan.com/address/${CONTRACT_ADDRESS}#code`);
      return;
    }
    
    // If contract is not verified, attempt to attach ABI directly
    const abiParams = new URLSearchParams();
    abiParams.append('apikey', POLYGONSCAN_API_KEY);
    abiParams.append('module', 'contract');
    abiParams.append('action', 'insertcontractabi');
    abiParams.append('address', CONTRACT_ADDRESS);
    abiParams.append('abi', JSON.stringify(ABI));
    
    console.log("Submitting ABI association request...");
    const abiResponse = await axios.post('https://api.polygonscan.com/api', abiParams);
    
    console.log("API Response:", abiResponse.data);
    
    if (abiResponse.data.status === '1') {
      console.log("✅ Success! ABI successfully associated with the contract!");
      console.log(`View contract on Polygonscan: https://polygonscan.com/address/${CONTRACT_ADDRESS}#readContract`);
    } else {
      console.log("❌ Failed to associate ABI:", abiResponse.data.result);
      console.log("\nPlease follow the manual verification steps in DETAILED_POLYGONSCAN_VERIFICATION.md");
    }
  } catch (error) {
    console.error("Error associating ABI:", error.message);
    
    if (error.response) {
      console.error("API Response:", error.response.data);
    }
    
    console.log("\nPlease follow the manual verification steps in DETAILED_POLYGONSCAN_VERIFICATION.md");
  }
}

attachABI().catch(console.error);